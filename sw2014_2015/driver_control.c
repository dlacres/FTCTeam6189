task main()
{

eraseDisplay();
	while(true)
	{
		getJoystickSettings(joystick);

		/////////Joystick 1//////////


		motor[rtWheelMotor]=(joystick.joy1_y2+joystick.joy1_x2)/2;
		motor[ltWheelMotor]=(joystick.joy1_y2-joystick.joy1_x2)/2;

		////////Joystick 2//////////

		motor[sweeper]=joystick.joy2_y2;

if(joy2Btn(5)&&joy2Btn(4))
	{
		motor[lift]=joystick.joy2_y1;
	}
		if(joy2Btn(0))//Position 0: Bottom position
			{
			RaiseArm(25,60);
			}

			if(joy2Btn(1))//Position 1: Smallest rolling goal
			{
			RaiseArm(25,3241);
			}

				if(joy2Btn(2))//Position 2: Medium rolling goal
			{
			RaiseArm(25,14358);
			}

			writeDebugStreamLine("%d", nMotorEncoder[lift]);

			wait1Msec(50);

			if(joy2Btn(7))
				{
					Clampgoal(true);
				}
					else
					{
						Clampgoal(false);
					}

				if(joy2Btn(8))
				{
					DumpBucket(true);
				}
					else
					{
						DumpBucket(false);
					}



	}
}
