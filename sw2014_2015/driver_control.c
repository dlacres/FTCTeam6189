#include "JoystickDriver.c"
#include "i_clamp.c"
#include "i_dump.c"
#include "i_arm.c"
#pragma DebuggerWindows("JoystickGame")
#include "i_lookup.c"

int jstickX;
int jstickY;
int gyroBias=0;

void initializeRobot()
{
	servo(dump)=195;
	servo(clamp)=70;

	for (int i=0; i<5; i++){
		gyroBias = SensorValue[gyro]+gyroBias;
		wait1Msec(50);
	}
	gyroBias=gyroBias/5;


	return;
}
task main()
{
	initializeRobot();
	waitForStart();

	while(true)
	{
		getJoystickSettings(joystick);

		/////////Joystick 1//////////

		jstickX = Lookup1(joystick.joy1_x2); // No gyro
		//jstickX = Lookup1(joystick.joy1_x2) - DeadZone((SensorValue[gyroSensor]-gyroBias)/2,5); // Gyro
		jstickY = Lookup1(joystick.joy1_y2);

		// ------- Control the drive motors ----------//
		//motor[ltMotor]=(jstickY + jstickX);
		//motor[rtMotor]=(jstickY - jstickX);

		motor[rtWheelMotor]=(joystick.joy1_y2-joystick.joy1_x2)/2;
		motor[ltWheelMotor]=(joystick.joy1_y2+joystick.joy1_x2)/2;

		////////Joystick 2//////////

		motor[sweeper]=joystick.joy2_y2;


		if(joy2Btn(3))
		{
			motor[lift]=joystick.joy2_y1;
		}

		if(joy2Btn(1))//Position 0: Bottom position
		{
			RaiseArm(-25,120);
		}

		if(joy2Btn(2))//Position 1: Smallest rolling goal
		{
			RaiseArm(25,7900);
		}

		writeDebugStreamLine("%d", nMotorEncoder[lift]);

		if(joy2Btn(7))
		{
			Clampgoal(true);
		}
		else
		{
			Clampgoal(false);
		}

		if(joy2Btn(8))
		{
			DumpBucket(true);
		}
		else
		{
			DumpBucket(false);
		}

		wait1Msec(50);
	}
}
