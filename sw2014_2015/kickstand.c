#define FORWARD 0
#define IR 1
#define POSITION_1 2
#define POSITION_2 3
#define POSITION_3 4

int sm = FORWARD;

#define ENCODER_SET 0
#define MOVE 1

int sm2 = ENCODER_SET;

#include "JoystickDriver.c"  //Include file to "handle" the Bluetooth messages.
#include "i_forward.c"
#include "i_score.c"
#include "i_clamp.c"
#include "i_turn.c"

void initializeRobot()
{
servo(dump)=100;
servo(clamp)=0;
servo[score]=120;

forward_initizalize();


  return;
}

task main()
{

  initializeRobot();

  waitForStart();

  while(true)
  {
    switch(sm)
		{

		case FORWARD:

		if (forward(50,4000)==true)
		{

		sm=IR;
		}

		break;

		case IR:

		if (SensorValue[IR]==3)
		{
		sm=POSITION_1;
		}

		if (SensorValue[IR]==5)
		{
		sm=POSITION_2;
		}

		if (SensorValue[IR]==7)
		{
		sm=POSITION_3;
		}

		break;

		case POSITION_1:

		Turnright(25,180);

		break;

		case POSITION_2:

		Turnleft(25,180);

		break;

		case POSITION_3:

		backward(25,4000);

		break;


		}
	}
}
