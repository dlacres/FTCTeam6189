//*!!Code automatically generated by 'ROBOTC' configuration wizard               !!*//



#define FORWARD 0
#define AUTO_SCORE 1
#define GRAB 2
#define TURN 3
#define BACKWARD 4
#define STOP 5


int sm = FORWARD;


#include "JoystickDriver.c"  //Include file to "handle" the Bluetooth messages.
#include "i_forward.c"
#include "i_score.c"
#include "i_clamp.c"
#include "i_turn.c"

void initializeRobot()
{
servo(dump)=150;
servo(clamp)=0;
servo[score]=120;

forward_initialize();

  return;
}

#pragma DebuggerWindows("JoystickGame")

task main()
{
  initializeRobot();

  waitForStart();

  while(true)
  {
    switch(sm)
		{
		case FORWARD:

		  forward(75,8500);

		  forward(15,1950);

		  sm=AUTO_SCORE;

		break;


  	case AUTO_SCORE:

  	Auto_Score();
  	if(Auto_Score())
		{
			sm=GRAB;
		}

		break;

		case GRAB:

			servo[clamp]=70;

			wait1Msec(1000);

			sm=TURN;


		break;

		case TURN:

			Turnleft(25,45);


			sm=BACKWARD;


		break;

		case BACKWARD:
		backward(25,6000);

		if(backward(25,6000)==true)
		{
		sm=STOP;
		}

		break;

		case STOP:

		motor[rtWheelMotor]=0;
		motor[ltWheelMotor]=0;
		servo[clamp]=140;

		break;
  	}
	}
}
